// Problem Description
// Given nxn matrix where each of the rows and columns are sorted in ascending order, find the kth 
// smallest element in the matrix.
// Note that it is the kth smallest element in the sorted order, not the kth distinct element.

// Input format
// First line contains two integers. N, denoting the size of the square matrix and K where we have to 
// find the Kth Smallest element.
// Next N lines contain N integers each denoting the elements of the matrix.

// Output format
// Single containing one integer denoting the Kth smallest element.

// Sample Input --->
// 3 8
// 1 5 9
// 10 11 13
// 12 13 15

// Sample Output --->
// 13

// Explanation --->
// If written in non-decreasing order, elements are,
// 1 5 9 10 11 12 13 13 15
// Hence, 8th smallest element is 13.